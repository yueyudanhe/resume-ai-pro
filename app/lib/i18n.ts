// Helper function to get locale from country code
export function getLocaleFromCountry(countryCode: string): Locale {
  const upperCode = countryCode.toUpperCase();
  for (const locale of supportedLocales) {
    if (locale.region.includes(upperCode)) {
      return locale.code;
    }
  }
  return "en"; // Default to English
}

// Helper function to get locale info
export function getLocaleInfo(code: Locale): LocaleInfo | undefined {
  return supportedLocales.find((l) => l.code === code);
}
